#include <vector>
#include <functional>
class Solution {
public:
    std::vector<int> preorderTraversal(TreeNode* root) {
        std::vector<int> result;
        std::function<void(TreeNode*)> backtracking = [&](TreeNode* node){
            if(!node) return;
            result.push_back(node->val);
            backtracking(node->left);
            backtracking(node->right);
        };    
        backtracking(root);
        return result;
    }
};
