class Solution {
public:
    ListNode* swapPairs(ListNode* head) {
        if(!head) return head;
        ListNode * dummyHead = new ListNode(0,head);
        ListNode * cur = dummyHead;
        ListNode * nxt = dummyHead->next;
        if(!nxt->next){
            // dummyHead->next = nullptr;
            // delete dummyHead;
            return nxt;
        }        
        ListNode * nxt2 = nxt->next;
        while(cur && nxt &&nxt2){
            cur->next = nxt2;
            nxt ->next = nxt2->next;
            nxt2->next = nxt;

            cur = nxt;
            nxt = (cur && cur->next)?cur->next:nullptr;
            nxt2 = (nxt && nxt->next)?nxt->next:nullptr;
        }
        return dummyHead->next;
    }
};
